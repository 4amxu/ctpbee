def close_program(keyword):
    import os
    command = f"ps aux | grep {keyword}"
    f = os.popen(command)
    results = f.readlines()
    print(results)
    pids = [c.split()[1] for c in results if "grep" not in c]
    print(pids)
    for pid in pids:
        os.system(f"kill -9 {pid}")
